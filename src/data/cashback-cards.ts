export interface CardTier {
  minMonthlySpend: number;     // RM spend required to unlock this tier
  categories: string[];        // Which spending categories earn at this rate
  rate: number;                // Decimal, e.g. 0.05 = 5%
  categoryCap: number;         // Max RM cashback per individual category (0 = unlimited)
  poolCap: number;             // Max RM cashback across all categories in this tier (0 = unlimited)
  isWeekendOnly: boolean;      // true = cashback only on Sat/Sun
}

export interface CreditCard {
  id: string;
  bank: string;
  cardName: string;
  hexColor: string;            // Primary brand hex — used for gradient, no logos
  network: 'Visa' | 'Mastercard' | 'UnionPay' | 'Amex';
  minIncome: number;           // Annual income in RM
  annualFee: number;           // RM per year (0 = free)
  feeWaiverCondition: string;  // Human-readable waiver condition
  baseCashbackRate: number;    // Fallback rate if min spend not met (e.g. 0.001)
  tiers: CardTier[];
}

